void drawNumber(int num, byte frame[8][12]){
  int ones = num % 10;
  int tens = (num - ones) / 10;
  for(int i=0;i<8;i++){
    for(int j=0;j<12;j++){
      frame[i][j] = tensDigits[tens][i][j] + onesDigits[ones][i][j];
    }
  }
}
